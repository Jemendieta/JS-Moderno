// Operadores
// Mayor > y Menor <
const numero1 = 20;
const numero2 = "20";
const numero3 = 30;

// operador mayor
console.log(numero1 > numero3); // nos devolverá false
console.log(numero3 > numero1); // nos devolverá true

// opreador menor
console.log(numero1 < numero3); // nos devolverá true
console.log(numero3 < numero1); // nos devolverá false

console.log(numero3 < numero2); // nos devolverá false
