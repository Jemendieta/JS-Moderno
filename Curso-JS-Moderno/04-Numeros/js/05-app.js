// Incrementos con n√∫meros.
// incremento de 1 en 1
// Decremento de 1 en 1
let puntaje = 5;

// incremento de 1 en 1.
// usando el operador ++
puntaje++; //primero llama al puntaje y luego se incrementa

++puntaje; //primero incrementa y luego se muestra

// Decremento de 1 en 1
// Usando el operador --
let puntaje2 = 5;
puntaje2--; // primero llama ala variable y luego decrementa
--puntaje2; // primero decrementa y luego muestra el puntaje

// incremento o decremento en valores mas grandes
let puntaje3 = 5;

puntaje3 -= 2; // devuelve 3
puntaje3 += 2; // devuelve 7

console.log(puntaje);
console.log(puntaje2);
console.log(puntaje3);
