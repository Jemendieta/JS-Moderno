// Operaciones con números
const numero1 = 20;
const numero2 = 40;

let suma;
let resta;
let multiplicacion;
let division;
let modulo;

// Suma
suma = numero1 + numero2;
// Resta
resta = numero1 - numero2;
// Multiplicación
multiplicacion = numero1 * numero2;
// División
division = numero1 / numero2;
// Módulo
modulo = numero1 % numero2;

// Resultados
console.log(suma);
console.log(resta);
console.log(multiplicacion);
console.log(division);
console.log(modulo);
